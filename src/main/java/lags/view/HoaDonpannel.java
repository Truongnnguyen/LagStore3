        jTextField4.setText(String.valueOf(tblHoaDon.getValueAt(row, 3))); // Tên KH
        jTextField5.setText(String.valueOf(tblHoaDon.getValueAt(row, 5))); // SDT
        jTextField6.setText(String.valueOf(tblHoaDon.getValueAt(row, 4))); // Địa chỉ
